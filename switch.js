// Javascript Switch Case Break And Default

var switchCase = '5';
switch(+switchCase){
    case 1:
    console.log('Saturday');
    break;   

    case 2:
    console.log('sunday');
    break;

    case 3:
    console.log('Monday');
    break;

    case 4:
    console.log('Thuesday');
    break;

    case 5:
    console.log('Wendnesday');
    break;

    case 6:
    console.log('Thursday');
    break;

    case 7:
    console.log('Friday');
    break;

}